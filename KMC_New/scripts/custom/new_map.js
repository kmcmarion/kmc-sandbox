// Loop through mobileBranches
// Filter the mobileBranches with the selected item
// Pass the prop to setCity function
// Display the props in HTML format
$(document).ready(function () {
    $("#cityList").trigger("change")
})

$("#cityList").on("change blur", function () {
    $("#buildingList").text('')
    var city = $(this).val()
    for (branch in mobileBranches.features) {
        var prop = mobileBranches.features[branch].properties
        if (prop.city == city) {
            setCity(prop)
        }
    }

})


function setCity(prop) {
    $("#buildingList").append(`
    <div class='card mb-2 p-2'>
        <img class="card-img-top" src="${prop.image}" alt="Card image cap">
        <div class='card-body'>
            <h3 class='card-title font-weight-bold'><a href='http://lkmc.solutions${prop.link}'>${prop.buildingName}</a></h3>
            <p>${prop.address}</p>
            <p>${prop.phone}</p>
        </div>
    </div>
    `)
}